namespace DNTPersianUtils.Core
{
    /// <summary>
    /// DateTimeOffset Part
    /// </summary>
    public enum DateTimeOffsetPart
    {
        /// <summary>
        /// The date and time of the current System.DateTimeOffset object.
        /// </summary>
        DateTime,

        /// <summary>
        /// The local date and time of the current System.DateTimeOffset object.
        /// </summary>
        LocalDateTime,

        /// <summary>
        /// The Coordinated Universal Time (UTC) date and time of the current System.DateTimeOffset
        /// </summary>
        UtcDateTime
    }
}